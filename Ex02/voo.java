package Ex02;

public class voo {
    String companhia;
    String origem;
    String destino;
    String data;
    String horaSaida;
    String horaChegada;
    float valor;

    public voo (String companhia, String origem, String destino, String data, String horaSaida, String horaChegada, float valor) {
        this.companhia = companhia;
        this.origem = origem;
        this.destino = destino;
        this.data = data;
        this.horaSaida = horaSaida;
        this.horaChegada = horaChegada;
        this.valor = valor;
    }
}
