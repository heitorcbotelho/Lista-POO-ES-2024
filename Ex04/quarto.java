package Ex04;

public class quarto {
    String numeroQuarto;
    String totalCamas;
    String tipo; 
    float diaria;
    boolean disponibilidade;
    public quarto (String numeroQuarto, String totalCamas, String tipo, float diaria, boolean disponibilidade){
        this.numeroQuarto = numeroQuarto;
        this.totalCamas = totalCamas;
        this.tipo = tipo;
        this.diaria = diaria;
        this.disponibilidade = disponibilidade;
    }
}
